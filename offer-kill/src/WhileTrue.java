public class WhileTrue {

    public static void main(String[] args) {
        testFor();

        testWhile();

    }

    private static void testFor() {
        for(;;){
            System.out.println("this is hollis testing....");
        }
    }

    private static void testWhile() {
        while (true){
            System.out.println("this is hollis testing....");
        }
    }

}
